#ifndef COLORS_H
#define COLORS_H

/**
 * @file colors.h
 * @brief Defines color constants.
 *
 * This header provides hex color definitions used throughout the UI.
 * The colors are specified as 32-bit hexadecimal values (ARGB format).
 */

#define COLOR_PRIMARY 0x4CAF50FF        // Green
#define COLOR_PRIMARY_DARK 0x2E7D32FF   // Dark Green
#define COLOR_ACCENT 0x81C784FF         // Light Green
#define COLOR_SUCCESS 0xA5D6A7FF        // Success Green
#define COLOR_BG 0xF1F8E9FF             // Very Light Green Background
#define COLOR_DARK_BG 0x1B5E20FF        // Very Dark Green
#define COLOR_TEXT 0x1B5E20FF           // Dark Green Text
#define COLOR_TEXT_LIGHT 0xFFFFFFFF     // White Text

#endif // COLORS_HS
